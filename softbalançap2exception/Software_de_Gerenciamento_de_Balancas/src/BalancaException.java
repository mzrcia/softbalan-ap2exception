public class BalancaException extends Exception {
    public BalancaException() {
        super();
    }

    public BalancaException(String message) {
        super(message);
    }

    public BalancaException(String message, Throwable cause) {
        super(message, cause);
    }

    public BalancaException(Throwable cause) {
        super(cause);
    }
}
